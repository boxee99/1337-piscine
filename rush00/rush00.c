#include <unistd.h>

void	ft_putchar(char c);

void	rush(int x, int y)
{
	int	i;
	int	j;

	i = 0;
	while (i < y && (x > 0 && y > 0))
	{
		j = 0;
		while (j < x)
		{
			if ((i == 0 && (j == 0 || j == x - 1))
				|| (i == y - 1 && (j == 0 || j == x - 1)))
				ft_putchar('o');
			else if (i == 0 || i == y - 1)
				ft_putchar('-');
			else if (j == 0 || j == x - 1)
				ft_putchar('|');
			else
				ft_putchar(' ');
			j++;
		}
		ft_putchar('\n');
		i++;
	}
}
